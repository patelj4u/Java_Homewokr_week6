package week6program;
//Write a program to convert the upper case to lower case.

public class Prg9_Upper_Lower {
    public static void main(String[] args) { //Main Method
        String input1 = "I MY LOVE INDIA";//Upper case
        String input2 = "i also love uk";// Lower case

       // System.out.println(input1);
        System.out.println(input1.toLowerCase());//Print Statement

        System.out.println(input2.toUpperCase());//Print Statement
    }
}
